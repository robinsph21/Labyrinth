package cs301.up.edu.labyrinth.enums;

/**
 * Defines an treasure enumeration that represents one of the different types
 * of treasures that can be found on the board. Note: all tiles will have a
 * treasure type but NONE is an option
 *
 * @author Erik Torkelson, Spencer Nelson, Spencer Rose, Philip Robinson
 * Date: 2/27/2019
 */
public enum TreasureType {
    NONE, BAT, BOOK, BOW, CANDLES, CHEST, COINS, CROWN, DRAGON, FOX, GEM, GHOST,
    GOBLET, HELMET, KEYS, MAP, MOTH, MOUSE, OWL, RING, SHIELD, SKULL,
    SPIDER, SWORD, URN
}
